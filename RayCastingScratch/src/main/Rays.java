	public void update(KeyHandler keyH) {
		if (keyH.rayResPlus) {
			ray_count += 1;
			System.out.println(ray_count);
		} if (keyH.rayResMinus && ray_count>1) {
			ray_count -= 1;
			System.out.println(ray_count);
		}
	}
	
	public void draw(Graphics2D g2, KeyHandler keyH) {
		
		px = (int) gp.player.x;
		py = (int) gp.player.y;
		
		Line2D vertical;
		Line2D horizontal;
		
		ra = gp.player.a-DR*(ray_count/2)*64/ray_count;
		
		if (ra<0) {
			ra += 2*PI;
		} else if (ra > 2*PI) {
			ra -= 2*PI;
		}

		Line2D ray;
		int dir;
		int mapVal;
		
		///////////////////////

		double lineH;

		int lineO; 
		double ca;
		double Width = gp.screenWidth/ray_count;
		double Height = gp.screenHeight;
		double HorizontalOff = 0;
		
		double ty, tx; //texture x,y
		double ty_step, ty_offset;
		int red, green, blue, c;
		double shade;
		
		for (r=0;r<ray_count;r++) {

			// HORIZONTAL LINES 
			dof = 0;
			double aTan = -1/Math.tan(ra);
			
			if(ra>PI) {
				ry = (((int)py>>6)<<6)-0.0001;
				rx = (py-ry)*aTan+px;
				yo = -64;
				xo = -yo*aTan;
			} else if (ra<PI) {
				ry = (((int)py>>6)<<6) + 64;
				rx = (py-ry)*aTan+px;
				yo = 64;
				xo = -yo*aTan;
			} else if (ra==0 || ra==PI) {
				rx=px;
				ry=py;
				dof=8;
			}
			while (dof<8) {
				mx = (int) rx>>6;
				my = (int) ry>>6;
				
				if (mx>=0&&mx<8&&my>=0&&my<8&&map.mapW[my][mx] > 0) {
					dof=8;
				} else {
					rx += xo;
					ry += yo;
					dof += 1;
				}
			}
			
			horizontal = new Line2D.Double(px, py, rx, ry);
		
		
			// VERTICAL LINES
			dof = 0;
			double nTan = -Math.tan(ra);
			
			if(ra>PI2 && ra<PI3) {
				rx = (((int)px>>6)<<6)-0.0001;
				ry = (px-rx)*nTan+py;
				xo = -64;
				yo = -xo*nTan;
			} else if (ra<PI2 || ra>PI3) {
				rx = (((int)px>>6)<<6) + 64;
				ry = (px-rx)*nTan+py;
				xo = 64;
				yo = -xo*nTan;
			} else if (ra==0 || ra==PI) {
				rx=px;
				ry=py;
				dof=8;
			}
			while (dof<8) {
				mx = (int) rx>>6;
				my = (int) ry>>6;
				
				if (mx>=0&&mx<8&&my>=0&&my<8&&map.mapW[my][mx] > 0) {
					dof=8;
				} else {
					rx += xo;
					ry += yo;
					dof += 1;
				}
			}
			
			if (ra<0) {
				ra += 2*PI;
			} else if (ra > 2*PI) {
				ra -= 2*PI;
			}
			
			vertical = new Line2D.Double(px, py, rx, ry);
			
			if (Math.min(length(vertical), length(horizontal)) == length(vertical)) {
				ray = vertical;
				dir = 1;
			} else {
				ray = horizontal;
				dir = 0;
			}
			mx = (int) ray.getX2()>>6;
			my = (int) ray.getY2()>>6;
			mapVal = map.mapW[my][mx];
			
			ra += DR*64/ray_count;
			if (ra<0) {
				ra += 2*PI;
			} else if (ra > 2*PI) {
				ra -= 2*PI;
			}
		
			/////////////////////	
			
			ca = DR*(ray_count/2)*64/ray_count-DR*r*64/ray_count;
			
			if (ca<0) {
				ca += 2*PI;
			} else if (ca > 2*PI) {
				ca -= 2*PI;
			}
			ca = Math.cos(ca);
			
			
			if (dir == 1) {
				shade = 0.5;
			} else {
				shade = 1;
			}
			
			//g2.setStroke(new BasicStroke(1));
			//g2.setColor(new Color(100, 100, 100));
			//g2.draw(ray);
			
			lineH = (gp.mapSize*Height/(length(ray)*ca));
			ty_step = 32.0/lineH;
			ty_offset = 0; 
			if (lineH>Height) {
				ty_offset = (lineH-Height)/2.0;
				lineH = Height;
			}
			lineO = (int) ((Height/2)-lineH/2);
			
			
			//Draw Walls
			g2.setStroke(new BasicStroke((float) (Width)));
			
			ty = ty_offset*ty_step+(mapVal-1)*32;
			rx = ray.getX2();
			ry = ray.getY2();
			if (dir == 0) {
				tx = (int) (rx/2.0)%32;
				if (ra<PI) {
					tx = 31-tx;
				}
			} else {
				tx = (int) (ry/2.0)%32;
				if (ra<PI*3/2 && ra>PI/2) { 
					tx = 31-tx;
				}
			}
			
			//g2.draw(new Line2D.Double((double) (line_num*Width)+512+(Width/2), lineO, (double) (line_num*Width)+512+(Width/2), lineH+lineO));
			
			for (int y=0;y<lineH;y++) {
				c = All_Textures[(int) ((int) (ty)*32 + (int) (tx))];
				red=0; green=0; blue=0;
				switch (mapVal) {
				case 1:
					blue = (int) (c*200*shade);
					break;
				case 2:
					red = (int) (c*200*shade);
					break;
				case 3:
					blue = (int) (c*200*shade);
					green = (int) (c*100*shade);
					break;
				case 4:
					green = (int) (c*200*shade);
					break;
				}
				
				g2.setColor(new Color(red, green, blue));
				g2.draw(new Line2D.Double((double) (r*Width)+HorizontalOff+(Width/2), y+lineO, (double) (r*Width)+HorizontalOff+(Width/2), y+lineO));
				ty+=ty_step;
			}
			
			//Draw Floor / Ceiling

			for(int y=(int) (lineO+lineH);y<Height;y++) {
				double dy=y-(Height/2.0);
				tx=gp.player.x/2 + Math.cos(ra-PI)*316*32/dy/Math.cos(gp.player.a-ra+PI);
				ty=gp.player.y/2 - Math.sin(ra)*316*32/dy/Math.cos(gp.player.a-ra+PI);
				
				//floor
				mapVal = map.mapF[(int)(ty/32.0)][(int)(tx/32.0)];
				c = All_Textures[(int) (((int) (ty)&31)*32 + ((int) (tx)&31))+(mapVal-1)*32*32];
				red=0; green=0; blue=0;
				switch (mapVal) {
				case 1:
					blue = (int) (c*200*0.7);
					break;
				case 2:
					red = (int) (c*200*0.7);
					break;
				case 3:
					blue = (int) (c*200*0.7);
					green = (int) (c*100*0.7);
					break;
				case 4:
					green = (int) (c*200*0.7);
					break;
				}
				g2.setColor(new Color(red, green, blue));
				g2.draw(new Line2D.Double((double) ((r)*Width)+HorizontalOff+(Width/2), y, (double) ((r)*Width)+HorizontalOff+(Width/2), y));
				
				//ceiling
				mapVal = map.mapC[(int)(ty/32.0)][(int)(tx/32.0)];
				c = All_Textures[(int) (((int) (ty)&31)*32 + ((int) (tx)&31))+(mapVal-1)*32*32];
				red=0; green=0; blue=0;
				switch (mapVal) {
				case 1:
					blue = (int) (c*200*0.7);
					break;
				case 2:
					red = (int) (c*200*0.7);
					break;
				case 3:
					blue = (int) (c*200*0.7);
					green = (int) (c*100*0.7);
					break;
				case 4:
					green = (int) (c*200*0.7);
					break;
				}
				g2.setColor(new Color(red, green, blue));
				//g2.draw(new Line2D.Double((double) ((r)*Width)+HorizontalOff+(Width/2), Height-y, (double) ((r)*Width)+HorizontalOff+(Width/2), Height-y));
			}
			
		}
	}

	public double length(Line2D line) {
		return (Math.sqrt(Math.pow((line.getX1()-line.getX2()), 2) + Math.pow((line.getY1()-line.getY2()), 2)));
	}
	
